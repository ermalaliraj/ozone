package al.ozone.bl.bean;

public class TopCustomersBean {

	private String email;
	private String nrPurchases;
	private String moneySpent;
	
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getNrPurchases() {
		return nrPurchases;
	}
	public void setNrPurchases(String nrPurchases) {
		this.nrPurchases = nrPurchases;
	}
	public String getMoneySpent() {
		return moneySpent;
	}
	public void setMoneySpent(String moneySpent) {
		this.moneySpent = moneySpent;
	}
	
}
